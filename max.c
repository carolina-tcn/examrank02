int	max(int *tab, unsigned int len)
{
	int	i = 0;
	if (!len) 
		return (0);
	while (i < len)
	{
		if (tab[0] < tab[i])
			tab[0] = tab[i];
		i++;
	}
	return (tab[0]);
}
